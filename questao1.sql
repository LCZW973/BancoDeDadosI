CREATE TABLE empresa.Projeto (
PNOME VARCHAR(15),
PNUMERO  INTEGER NOT NULL ,
PLOCALIZACAO VARCHAR(20),
DNUMERO_DEPARTAMENTO INTEGER NOT NULL,

PRIMARY KEY (PNUMERO),
FOREIGN KEY (DNUMERO_DEPARTAMENTO) REFERENCES empresa.Departamento (DNUMERO) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE empresa.Dependente(
SSN_EMPREGADO BIGINT NOT NULL ,
NOME_DEPENDENTE VARCHAR(15) ,
SEXO BIT ,
DATANASC DATE NOT NULL ,
PARENTESCO VARCHAR(15) NOT NULL,

PRIMARY KEY(SSN_EMPREGADO,NOME_DEPENDENTE),
FOREIGN KEY (SSN_EMPREGADO) REFERENCES empresa.Empregado (SSN) ON DELETE CASCADE ON UPDATE CASCADE 
);

CREATE TABLE empresa.Deptolocais(
LNUMERO BIGINT NOT NULL ,
DLOCALIZACAO VARCHAR(15) NOT NULL,
DNUMERO_DEPARTAMENTO INTEGER NOT NULL ,

PRIMARY KEY (LNUMERO),
FOREIGN KEY (DNUMERO_DEPARTAMENTO) REFERENCES empresa.Departamento (DNUMERO) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE empresa.trabalhaem(
SSN_EMPREGADO BIGINT NOT NULL,
PNUMERO_PROJETO INTEGER NOT NULL  ,
HORAS DECIMAL (4,2) ,

PRIMARY KEY (SSN_EMPREGADO,PNUMERO_PROJETO),
FOREIGN KEY (SSN_EMPREGADO) REFERENCES empresa.Empregado (SSN) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (PNUMERO_PROJETO) REFERENCES empresa.Projeto (PNUMERO) ON DELETE CASCADE ON UPDATE CASCADE
);
