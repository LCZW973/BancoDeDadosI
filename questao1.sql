CREATE DATABASE empresa;
CREATE TABLE empresa.Empregado
(
PNOME VARCHAR(15) NOT NULL,
MINICIAL CHAR,
UNOME VARCHAR(15)   , 
SSN BIGINT NOT NULL   ,
DATANASC DATE ,
DENDERECO VARCHAR (80),
SEXO BIT ,
SALARIO DECIMAL(10,2),
SSN_SUPERVISOR BIGINT NOT NULL DEFAULT 1,
DNUMERO_DEPARTAMENTO INTEGER   ,

PRIMARY KEY (SSN),
FOREIGN KEY (SSN_SUPERVISOR) REFERENCES empresa.Empregado (SSN)  ON DELETE SET  DEFAULT ON UPDATE CASCADE
);
 
