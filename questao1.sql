CREATE DATABASE empresa;

CREATE TABLE empresa.Empregado
(
PNOME VARCHAR(15) NOT NULL,
MINICIAL CHAR,
UNOME VARCHAR(15)   , 
SSN BIGINT NOT NULL   ,
DATANASC DATE ,
DENDERECO VARCHAR (80),
SEXO BIT ,
SALARIO DECIMAL(10,2),
SSN_SUPERVISOR BIGINT ,
DNUMERO_DEPARTAMENTO INTEGER NOT NULL DEFAULT 1   ,

PRIMARY KEY (SSN),
FOREIGN KEY (SSN_SUPERVISOR) REFERENCES empresa.Empregado (SSN)  ON DELETE SET  NULL ON UPDATE CASCADE
);
 

CREATE TABLE empresa.Departamento(
DNOME VARCHAR(15),
DNUMERO INTEGER NOT NULL DEFAULT 1 ,
SSN_EMPREGADO BIGINT  ,
DATAINICIO DATE ,

PRIMARY KEY (DNUMERO),
FOREIGN KEY (SSN_EMPREGADO) REFERENCES empresa.Empregado (SSN) ON  DELETE SET NULL ON UPDATE CASCADE 
);  

