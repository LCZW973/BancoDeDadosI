CREATE TABLE empresa.Departamento(
DNOME VARCHAR(15),
DNUMERO INTEGER NOT NULL ,
SSN_EMPREGADO BIGINT NOT NULL DEFAULT 1  ,
DATAINICIO DATE ,

PRIMARY KEY (DNUMERO),
FOREIGN KEY (SSN_EMPREGADO) REFERENCES empresa.Empregado (SSN) ON DELETE DEFAULT ON UPDATE CASCADE 
);  


